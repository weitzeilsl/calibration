        <h2>Family History Management Tool</h2>

        <p>This tool was developed to provide a consistent method for evaluating our employees and to help raise our organization to higher standards.  We believe that this will happen as we reward and recognize those who are high performing and correct, encourage, coach and mentor those who are not performing
to their full capacity.  Excellent managers do all of these things.</p>
        <p>We encourage managers to utilize a variety of resources as they evaluate their employees performance:</p>

        <ul>
    
            <li><b>Self Evaluation</b> - The Competencies tab provides a form for employees  or managers to evaluate themselves against approved competencies for their role.</li>
            <li><b>Manager Evaluation</b> - Managers should also consider the employees performance against their competencies.  They consider the
                                               employee's completion of division goals and feedback received during weekly 1:1's.  The Eval tabs (e.g., 92 Eval)
                                               provide a location for Managers to record the evaluation for each of their employees in their respective grade levels.</li>
            <li><b>360 Degree Feedback</b> - Managers should look for opportunities receive feedback from the teammates of their employees.  We do not believe
                                               that this type of feedback must be required, but we do believe that it can be an effective approach to gathering
                                               additional feedback for the employee.</li>
            <li><b>Team Stack Ranking</b> - Employees of the same grade level should be stack ranked together.  This process helps management to identify those
                                               who are high performers and those who may be in need of improvement.  This also helps us to identify the caliber of
                                               of new hires needed to lift the organization to higher levels.  The council does recommend that manager evaluations
                                               should be completed before the team stack ranks are started.</li>
        </ul>
        <p>AMI calculators are provided for each grade level. Managers should fill out the required information for their team members, however, the final approval for salary recommendations must come from the Group Manager.  It is expected that managers will not communicate their evaluation information until Group Managers have calibrated all of their managers and given the go-ahead to do so.</p>

        <p>Employees must be given clear concise communication regarding their performance.  However, managers should not communicate to employees their stack rank positions or the specific values given to them in their Evaluations.  The radar charts provided on the Evaluation tabs are for the manager's use only.</p>

