        <h3>AMI/ATI</h3>
        <p>The manager uses this worksheet to view the slider values to be entered into ATI.  A manager is not authorized to enter a different value into ATI or AMI than the one calculated here (without Group Manager aproval).   There are only two editable columns:<br>
        <lu>
            <li>The Group Manager Adjustment; and,</li>
            <li>Adjustment Justification.</li>
        </lu>
        <p>All other necessary information is pulled from the previous worksheets.</p>
