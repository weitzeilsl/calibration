        <h3>Evaluation</h3>
        <a name="Rank"></a>
        <p>As a manager, you use this worksheet to record your rating of the level 98 employees on your team.  Columns D2:L2 list the
            competencies that each employee is being evaluated against.  The complete definition for each of these competencies can be
            found in the Competency Profile document for your specific discipline.</p>
        <p>Prior to filling out this worksheet, each employee should have evaluated themselves on a scale of 1 to 5 (1 being the
            highest) against the competency profile for their discipline and level (the employee evaluation is recorded on the Self
            Evaluation worksheet).  The manager evaluates the employee's self evaluation and considers that information in combination
            with other information (e.g., 360 degree feedback) gathered during the evaluation period.</p>
        <p>The manager then records his/her evaluation of the employee's performance against the competency profile for the employee's
            level.  The  score for the employee is then calculated in the  Eval Score field (column M).   An average Eval Score is
            provided to help the manager know if his team is being ranked above or below the 3.0 mid-point.</p>
        <p>Radar charts are auto-generated for each employee.  However, the name on each radar chart must be manually entered.  These
            charts should not be shown to the employee, but are a helpful visual tool for the manager to quickly see where the employee
            excels and/or needs improvement.</p>
